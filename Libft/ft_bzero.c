#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (*((unsigned char *)s + i) != 0)
			*((unsigned char *)s + i) = 0;
		++i;
	}
}
